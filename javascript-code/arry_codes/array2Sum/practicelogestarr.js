// // find the longest  aaray sum


function longestArraySum(arr, k) {
    let n = arr.length
    let hashmap = new Map()
    let sum = 0
    let maxLen = 0

    for (let i = 0; i < n; i++) {
        sum += arr[i]
       

        if (sum === k) {
            maxLen = Math.max(maxLen, i + 1)
           
        }

        let rem = sum - k
        if(hashmap.has(rem)){
            length = i-hashmap.get(rem)
            maxLen = Math.max(maxLen,length)
            
        }
        if(!hashmap.has(sum)){
            hashmap.set(sum,i)
        }
        
    }
    return maxLen
}
